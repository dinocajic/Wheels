package edu.gsu.student.wheels;

public class Wheel {

    public String brand;
    public String model;
    public String bolts;
    public String bolt_pattern;
    public String image;
    public String price;
    public String item_number;
    public String diameter;
    public String width;

    public String getDiameter() {
        return diameter;
    }

    public void setDiameter(String diameter) {
        this.diameter = diameter;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getOffset() {
        return offset;
    }

    public void setOffset(String offset) {
        this.offset = offset;
    }

    public String offset;

    public String getItem_number() {
        return item_number;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getBolts() {
        return bolts;
    }

    public void setBolts(String bolts) {
        this.bolts = bolts;
    }

    public String getBolt_pattern() {
        return bolt_pattern;
    }

    public void setBolt_pattern(String bolt_pattern) {
        this.bolt_pattern = bolt_pattern;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public void setItem_number(String item_number) {
        this.item_number = item_number;
    }
}
